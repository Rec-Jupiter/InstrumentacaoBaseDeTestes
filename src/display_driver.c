void send_data(int data) {
    int reversed_data = 0;

    for (int i = 0; i <= 9; i++) {
        reversed_data |= (((data & (1 << i)) >> i) << (9 - i));
    }

    pio_sm_put_blocking(pio, sm, reversed_data);
}


void ST7920_graphic_mode(int enable) {
    if (enable == 1) {
        send_data(0b0000110000);
        sleep_ms(1);
        send_data(0b0000110100);
        sleep_ms(1);
        send_data(0b0000110110);
        sleep_ms(1);
    } else {
        send_data(0b0000110000);
        sleep_ms(1);
    }
}


void ST7920_draw_bitmap(const unsigned char* graphic) {
    uint8_t x, y;

    for (y = 0; y < 64; y++) {
        if (y < 32) {
            for (x = 0; x < 8; x++) {
                send_data(0x080 | y);
                sleep_us(300);
                send_data(0x080 | x);
                sleep_us(300);
                send_data(0x100 | (int)graphic[2*x + 16*y]);
                sleep_us(300);
                send_data(0x100 | (int)graphic[2*x + 16*y + 1]);
                sleep_us(300);
            }
        } else {
            for (x = 0; x < 8; x++) {
                send_data(0x080 | (y - 32));
                sleep_us(300);
                send_data(0x088 | x);
                sleep_us(300);
                send_data(0x100 | graphic[2*x + 16*y]);
                sleep_us(300);
                send_data(0x100 | graphic[2*x + 16*y + 1]);
                sleep_us(300);
            }
        }
    }
}


